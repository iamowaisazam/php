<?php

//Arithmetic Operators
//The PHP arithmetic operators are used with numeric values to perform common arithmetical operations, such as addition, subtraction, multiplication etc.

// 1.Addition
// 2.Substraction
// 3.Multiplication
// 4.Division
// 5.Modulus
// 6.Increment
// 7.Decrement
